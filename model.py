import pandas as pd

df = pd.read_csv('/content/Salary_Data (1).csv')

df

df.info()

df.isnull().sum()

# Fill numerical columns with mean
for col in ['Age', 'Years of Experience', 'Salary']:
    if df[col].isnull().any():
        df[col] = df[col].fillna(df[col].mean())

# Fill categorical columns with mode
for col in ['Gender', 'Education Level', 'Job Title']:
    if df[col].isnull().any():
        df[col] = df[col].fillna(df[col].mode()[0])

X = df.drop('Salary', axis=1)
y = df['Salary']

print('Input variables (X) shape:', X.shape)
print('Output variable (y) shape:', y.shape)

"""## Data Preprocessing for Model Training"""

X = df.drop('Salary', axis=1)
y = df['Salary']

# One-hot encode categorical features
X = pd.get_dummies(X, columns=['Gender', 'Education Level', 'Job Title'], drop_first=True)

print('Shape of X after one-hot encoding:', X.shape)
display(X.head())

"""## Splitting the Data"""

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2, random_state=43)

print('X_train shape:', X_train.shape)
print('X_test shape:', X_test.shape)
print('y_train shape:', y_train.shape)
print('y_test shape:', y_test.shape)

"""## Training and Evaluating Models"""

from sklearn.linear_model import LinearRegression
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor
from sklearn.metrics import r2_score, mean_absolute_error, mean_squared_error

models = {
    'Linear Regression': LinearRegression(),
    'Decision Tree': DecisionTreeRegressor(random_state=42),
    'Random Forest': RandomForestRegressor(random_state=42),
    'Support Vector Machine': SVR(),
    'K-Nearest Neighbors': KNeighborsRegressor()
}

results = {}

for name, model in models.items():
    print(f'Training {name}...')
    model.fit(X_train, y_train)
    y_pred = model.predict(X_test)

    r2 = r2_score(y_test, y_pred)
    mae = mean_absolute_error(y_test, y_pred)
    mse = mean_squared_error(y_test, y_pred)

    results[name] = {'R2 Score': r2, 'MAE': mae, 'MSE': mse}

    print(f'{name} - R2 Score: {r2:.4f}, MAE: {mae:.2f}, MSE: {mse:.2f}\n')

print('\n--- Model Performance Summary ---')
for name, metrics in results.items():
    print(f'{name}:')
    for metric_name, value in metrics.items():
        print(f'  {metric_name}: {value:.4f}')

import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd

# Convert results dictionary to a Pandas DataFrame for easier plotting
results_df = pd.DataFrame(results).T

# Create subplots for each metric
fig, axes = plt.subplots(1, 3, figsize=(20, 6))
fig.suptitle('Model Performance Comparison', fontsize=16)

# Plot R2 Score
sns.barplot(ax=axes[0], x=results_df.index, y='R2 Score', data=results_df, palette='viridis')
axes[0].set_title('R2 Score')
axes[0].set_ylabel('R2 Score')
axes[0].tick_params(axis='x', rotation=45)

# Plot MAE
sns.barplot(ax=axes[1], x=results_df.index, y='MAE', data=results_df, palette='magma')
axes[1].set_title('Mean Absolute Error (MAE)')
axes[1].set_ylabel('MAE')
axes[1].tick_params(axis='x', rotation=45)

# Plot MSE
sns.barplot(ax=axes[2], x=results_df.index, y='MSE', data=results_df, palette='cividis')
axes[2].set_title('Mean Squared Error (MSE)')
axes[2].set_ylabel('MSE')
axes[2].tick_params(axis='x', rotation=45)

plt.tight_layout(rect=[0, 0.03, 1, 0.95])
plt.show()



